#!/usr/bin/env node
/**
 * Pre-compile the design-system `--st-*` token CSS at BUILD time and emit it as
 * a static TS module (`src/workspace/tokens-st-css.generated.ts`).
 *
 * Why: `@sentropic/design-system-themes` is ESM-only (no CJS export). graphify
 * ships a CJS bundle too, so importing it at runtime breaks `require()`
 * (ERR_PACKAGE_PATH_NOT_EXPORTED). The token CSS is deterministic, so we compile
 * it once here (ESM, build-time only) and bundle the resulting string. The
 * themes package becomes a build-only devDependency, out of the runtime bundle.
 *
 * The dark-theme derivation mirrors what tokens-st.ts documented.
 */
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import {
  compileTheme,
  foundation,
  semantic,
  component,
  sentTechTheme,
} from "@sentropic/design-system-themes";

const ST_LIGHT_SELECTOR = ":root";
const ST_DARK_SELECTOR = ':root[data-ws-theme="dark"]';

const DARK_SEMANTIC = {
  ...semantic,
  surface: { ...semantic.surface, default: foundation.color.slate[90], subtle: foundation.color.slate[80], raised: foundation.color.slate[80], inverse: foundation.color.slate[0] },
  text: { ...semantic.text, primary: foundation.color.slate[10], secondary: "#94a3b8", inverse: foundation.color.slate[90] },
  border: { ...semantic.border, subtle: foundation.color.slate[80], strong: foundation.color.slate[60] },
};

const stDarkTheme = { id: "sent-tech-dark", label: "Sent Tech (dark)", mode: "dark", tokens: { foundation, semantic: DARK_SEMANTIC, component } };

const light = compileTheme(sentTechTheme, { selector: ST_LIGHT_SELECTOR });
const darkOptIn = compileTheme(stDarkTheme, { selector: ST_DARK_SELECTOR });
const darkAuto = compileTheme(stDarkTheme, { selector: ":root" });
const lightOnly = compileTheme(sentTechTheme, { selector: "__SELECTOR__" });

const fullCss = [
  "/* @sentropic/design-system-tokens + themes (compiled --st-* tokens). */",
  light,
  darkOptIn,
  "@media (prefers-color-scheme: dark) {",
  darkAuto,
  "}",
].join("\n");

const banner = `/**
 * AUTO-GENERATED by scripts/gen-st-tokens.mjs — do not edit by hand.
 * Pre-compiled @sentropic/design-system --st-* token CSS (themes is ESM-only,
 * so we bundle the compiled string instead of importing it at runtime).
 */`;

const out = `${banner}
/** Full --st-* stylesheet: light at :root, dark opt-in + prefers-color-scheme. */
export const ST_TOKENS_CSS = ${JSON.stringify(fullCss)};

/** Light-only --st-* block with a "__SELECTOR__" placeholder for the selector. */
const ST_LIGHT_TEMPLATE = ${JSON.stringify(lightOnly)};

/** Light --st-* block under a custom selector (for the standalone HTML export). */
export function stLightTokensCss(selector = ":root"): string {
  return ST_LIGHT_TEMPLATE.split("__SELECTOR__").join(selector);
}
`;

const dest = join(dirname(fileURLToPath(import.meta.url)), "..", "src", "workspace", "tokens-st-css.generated.ts");
writeFileSync(dest, out, "utf-8");
console.log(`gen-st-tokens: wrote ${dest} (${fullCss.length} bytes css)`);
