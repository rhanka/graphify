/**
 * Unit tests — git-flow branch-label legibility policy (SPEC_GITFLOW_LABELS,
 * ratified (b), Phases 1+2): P1.1 compaction table + ambiguity guard, P1.2
 * priority ordering + deterministic collision culling + hysteresis margin,
 * P1.3 interaction full-name labels (AC4), P2.1 LOD tiers with ±10%
 * hysteresis, P2.2 bounded fork→tip→stagger anchor fallback, AC5 determinism.
 */

import { describe, expect, it } from "vitest";
import {
  approximateLabelMeasure,
  compactGitFlowLabels,
  gitFlowLabelBoxHeightPx,
  compactGitFlowName,
  gitFlowNameHash,
  middleEllipsisPreservingSuffix,
  placeGitFlowLabels,
  resolveGitFlowLabelTier,
  DEFAULT_LABEL_MAX_CHARS,
  type GitFlowLabelCamera,
  type GitFlowLabelPolicyInput,
} from "../src/gitflow-labels";
import type { GitFlowBranchLabel } from "../src/layout-gitflow";
import { BOX_BASE_HEIGHT_PX } from "../src/render-geometry";

const CAMERA: GitFlowLabelCamera = { x: 0, y: 0, zoom: 1, width: 1600, height: 800 };

function label(partial: Partial<GitFlowBranchLabel> & { nodeIndex: number; name: string }): GitFlowBranchLabel {
  return {
    repo: "r",
    lane: 1,
    x: 0,
    y: 0,
    entry: "in-window",
    ...partial,
  };
}

function place(
  labels: GitFlowBranchLabel[],
  extra: Partial<Omit<GitFlowLabelPolicyInput, "labels" | "measure" | "camera">> & {
    camera?: Partial<GitFlowLabelCamera>;
  } = {},
  options = {},
) {
  return placeGitFlowLabels(
    {
      labels,
      measure: approximateLabelMeasure,
      camera: { ...CAMERA, ...extra.camera },
      interaction: extra.interaction,
      previous: extra.previous,
    },
    options,
  );
}

// ---------------------------------------------------------------------------
// P1.1 — compaction table.
// ---------------------------------------------------------------------------

describe("P1.1 semantic compaction", () => {
  it("compacts worktree-agent-<hash> to agent:<7hex> and demotes it", () => {
    const c = compactGitFlowName("worktree-agent-a2bfd3abf8ffc2032");
    expect(c.text).toBe("agent:a2bfd3a");
    expect(c.autogenerated).toBe(true);
    expect(compactGitFlowName("worktree-agent-ac2f8bac9020bc90b").text).toBe("agent:ac2f8ba");
  });

  it("compacts dependabot/<eco>/<pkg>-<ver> to deps:<pkg> and demotes it", () => {
    const c = compactGitFlowName("dependabot/npm_and_yarn/lodash-4.17.21");
    expect(c.text).toBe("deps:lodash");
    expect(c.autogenerated).toBe(true);
    expect(compactGitFlowName("dependabot/github_actions/actions/checkout-4").text).toBe(
      "deps:actions/checkout",
    );
  });

  it("strips refs/heads/ and origin/ then compacts conventional prefixes", () => {
    expect(compactGitFlowName("refs/heads/feature/cadrage").text).toBe("feat:cadrage");
    expect(compactGitFlowName("origin/fix/bug-123").text).toBe("fix:bug-123");
    expect(compactGitFlowName("feat/x")).toEqual({ text: "feat:x", autogenerated: false });
    expect(compactGitFlowName("chore/x").text).toBe("chore:x");
    expect(compactGitFlowName("hotfix/urgent").text).toBe("hotfix:urgent");
    expect(compactGitFlowName("release/1.2.0").text).toBe("rel:1.2.0");
  });

  it("leaves unconventional human names untouched", () => {
    expect(compactGitFlowName("b1-p2-edges")).toEqual({ text: "b1-p2-edges", autogenerated: false });
  });

  it("middle-ellipsis beyond ~22 chars PRESERVES the meaningful suffix", () => {
    const c = compactGitFlowName("feat/cadrage-geo-integration-extra-long");
    expect(c.text.length).toBeLessThanOrEqual(DEFAULT_LABEL_MAX_CHARS);
    expect(c.text.startsWith("feat:…")).toBe(true);
    expect(c.text.endsWith("extra-long")).toBe(true);
    // The raw helper keeps the tail when there is no compact prefix head.
    const raw = middleEllipsisPreservingSuffix("a-very-long-plain-branch-name-suffix", 22);
    expect(raw.length).toBe(22);
    expect(raw.endsWith("-suffix")).toBe(true);
  });

  it("ambiguity guard: distinct names colliding on one compacted string get hash suffixes", () => {
    const labels = [
      label({ nodeIndex: 0, name: "feat/one-CHANGED-part-geo-integration-suffix" }),
      label({ nodeIndex: 1, name: "feat/two-ALTERED-part-geo-integration-suffix" }),
    ];
    const [a, b] = compactGitFlowLabels(labels);
    expect(a!.text).not.toBe(b!.text);
    expect(a!.text).toMatch(/·[0-9a-f]{4}$/);
    expect(b!.text).toMatch(/·[0-9a-f]{4}$/);
    // Deterministic across calls.
    const again = compactGitFlowLabels(labels);
    expect(again.map((c) => c.text)).toEqual([a!.text, b!.text]);
  });

  it("ambiguity guard does NOT fire for identical full names (main per repo band)", () => {
    const labels = [
      label({ nodeIndex: 0, name: "main", repo: "a", lane: 0 }),
      label({ nodeIndex: 1, name: "main", repo: "b", lane: 0 }),
    ];
    const [a, b] = compactGitFlowLabels(labels);
    expect(a!.text).toBe("main");
    expect(b!.text).toBe("main");
  });

  it("configurable autogenerated rules extend the demoted class", () => {
    const c = compactGitFlowName("agy/run-42", {
      autogenerated: [{ pattern: /^agy\/run-(\d+)$/, replace: "agy:$1" }],
    });
    expect(c).toEqual({ text: "agy:42", autogenerated: true });
  });

  it("name hash is stable and deterministic", () => {
    expect(gitFlowNameHash("feat/alpha")).toBe(gitFlowNameHash("feat/alpha"));
    expect(gitFlowNameHash("feat/alpha")).not.toBe(gitFlowNameHash("feat/beta"));
  });
});

