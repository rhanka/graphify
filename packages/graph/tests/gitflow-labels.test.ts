describe("tier gating and interaction full names", () => {
  const scene = () => [
    label({ nodeIndex: 0, name: "main", lane: 0, x: 0, y: 0, tipX: 600 }),
    label({ nodeIndex: 1, name: "release/1.2", x: 60, y: 44, tipX: 240 }),
    label({ nodeIndex: 2, name: "feat/human-branch", x: 120, y: 88, tipX: 300 }),
    label({ nodeIndex: 3, name: "worktree-agent-abcdef0123456789a", x: 180, y: 132, tipX: 360 }),
    label({ nodeIndex: 4, name: "v-tag-ref", lane: -1, x: 600, y: 20, entry: "tip-only", tipX: 600 }),
  ];

  it("T0 (far): trunk + interaction only", () => {
    const p = place(scene(), { camera: { zoom: 0.05 } });
    expect(p.tier).toBe(0);
    expect(p.labels.map((l) => l.nodeIndex)).toEqual([0]);
  });

  it("T1 (mid): + human branches, still NO autogenerated pill (AC2 analog)", () => {
    const p = place(scene(), { camera: { zoom: 0.6 } });
    expect(p.tier).toBe(1);
    const ids = p.labels.map((l) => l.nodeIndex);
    expect(ids).toContain(0);
    expect(ids).toContain(1);
    expect(ids).toContain(2);
    expect(ids).toContain(4);
    expect(ids).not.toContain(3);
    expect(p.labels.some((l) => l.text.startsWith("agent:"))).toBe(false);
  });

  it("T2 (near): + autogenerated in compacted agent:<hash> form", () => {
    const p = place(scene(), { camera: { zoom: 2 } });
    expect(p.tier).toBe(2);
    const agent = p.labels.find((l) => l.nodeIndex === 3);
    expect(agent?.text).toBe("agent:abcdef0");
    expect(agent?.cls).toBe("autogenerated");
  });

  it("AC4: a culled/demoted branch marked SELECTED renders its full name at any tier", () => {
    const p = place(scene(), { camera: { zoom: 0.05 }, interaction: { selected: new Set([3]) } });
    expect(p.tier).toBe(0);
    const sel = p.labels.find((l) => l.nodeIndex === 3);
    expect(sel).toBeDefined();
    expect(sel!.fullName).toBe(true);
    expect(sel!.text).toBe("worktree-agent-abcdef0123456789a");
    expect(sel!.cls).toBe("interaction");
  });

  it("hovered and searched behave like selected (P1.3)", () => {
    const hov = place(scene(), { camera: { zoom: 0.05 }, interaction: { hovered: [3] } });
    expect(hov.labels.some((l) => l.nodeIndex === 3 && l.fullName)).toBe(true);
    const sea = place(scene(), { camera: { zoom: 0.05 }, interaction: { searched: [2] } });
    expect(sea.labels.some((l) => l.nodeIndex === 2 && l.text === "feat/human-branch")).toBe(true);
  });

  it("priority order: interaction ▸ trunk ▸ release ▸ feature ▸ tip-ref (acceptance order)", () => {
    const p = place(scene(), { camera: { zoom: 0.6 }, interaction: { selected: [2] } });
    const classes = p.labels.map((l) => l.cls);
    const firstOf = (c: string) => classes.indexOf(c as never);
    expect(firstOf("interaction")).toBe(0);
    expect(firstOf("trunk")).toBeGreaterThan(firstOf("interaction"));
    expect(firstOf("release")).toBeGreaterThan(firstOf("trunk"));
    expect(firstOf("tip-ref")).toBeGreaterThan(firstOf("release"));
  });

  it("feature recency: the newer tip places first within the class", () => {
    const labels = [
      label({ nodeIndex: 0, name: "feat/older", x: 0, y: 0, tipX: 100 }),
      label({ nodeIndex: 1, name: "feat/newer", x: 300, y: 0, tipX: 500 }),
    ];
    const p = place(labels);
    expect(p.labels[0]!.nodeIndex).toBe(1);
  });
});

// ---------------------------------------------------------------------------
// Label scale (−20% principal verdict, 2026-07-05): pills shrink font+padding
// together and the collision AABBs use the SHRUNKEN boxes.
// ---------------------------------------------------------------------------

describe("labelScale (−20% pills)", () => {
  it("gitFlowLabelBoxHeightPx: default 0.8 × legacy height; explicit overrides win", () => {
    expect(gitFlowLabelBoxHeightPx()).toBeCloseTo(BOX_BASE_HEIGHT_PX * 0.8, 10);
    expect(gitFlowLabelBoxHeightPx({ labelScale: 1 })).toBe(BOX_BASE_HEIGHT_PX);
    expect(gitFlowLabelBoxHeightPx({ labelScale: 0.5 })).toBeCloseTo(BOX_BASE_HEIGHT_PX * 0.5, 10);
    expect(gitFlowLabelBoxHeightPx({ boxHeightPx: 12, labelScale: 1 })).toBe(12);
  });

  it("placed pill AABBs shrink exactly 20% (height AND width) vs labelScale 1", () => {
    const labels = [label({ nodeIndex: 0, name: "feat/some-branch", x: 0, y: 0 })];
    const legacy = place(labels, {}, { labelScale: 1 }).labels[0]!;
    const scaled = place(labels).labels[0]!; // default labelScale 0.8
    expect(scaled.h).toBeCloseTo(legacy.h * 0.8, 6);
    expect(scaled.w).toBeCloseTo(legacy.w * 0.8, 6);
    // The culling used the SHRUNKEN box: at 0.8 the AABB is strictly smaller.
    expect(scaled.h).toBeCloseTo(BOX_BASE_HEIGHT_PX * 0.8, 6); // zoom 1, pr 1
  });

  it("shrunken pills relieve collision pressure (more labels survive culling)", () => {
    const labels: GitFlowBranchLabel[] = [];
    for (let i = 0; i < 40; i += 1) {
      labels.push(
        label({
          nodeIndex: i,
          name: `feat/branch-${String(i).padStart(2, "0")}`,
          x: (i % 6) * 30,
          y: Math.floor(i / 6) * 6,
          tipX: (i % 6) * 30 + 120,
        }),
      );
    }
    const scaled = place(labels); // default 0.8
    const legacy = place(labels, {}, { labelScale: 1 });
    expect(scaled.labels.length).toBeGreaterThanOrEqual(legacy.labels.length);
  });
});
