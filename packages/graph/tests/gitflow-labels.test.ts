// P2.1 — LOD tiers with ±10% hysteresis.
// ---------------------------------------------------------------------------

describe("P2.1 zoom LOD tiers", () => {
  it("resolves base tiers without previous state", () => {
    expect(resolveGitFlowLabelTier(0.05)).toBe(0);
    expect(resolveGitFlowLabelTier(0.5)).toBe(1);
    expect(resolveGitFlowLabelTier(2.0)).toBe(2);
  });

  it("±10% hysteresis: the previous tier sticks inside the band", () => {
    // t1 default 0.3: from T0, zoom 0.31 (< 0.33) stays T0…
    expect(resolveGitFlowLabelTier(0.31, 0)).toBe(0);
    // …but a fresh camera at 0.31 is T1, and from T1 zoom 0.29 (> 0.27) stays T1.
    expect(resolveGitFlowLabelTier(0.31)).toBe(1);
    expect(resolveGitFlowLabelTier(0.29, 1)).toBe(1);
    // Leaving the band commits the change.
    expect(resolveGitFlowLabelTier(0.34, 0)).toBe(1);
    expect(resolveGitFlowLabelTier(0.26, 1)).toBe(0);
  });

  it("crosses multiple tiers in one jump", () => {
    expect(resolveGitFlowLabelTier(3.0, 0)).toBe(2);
    expect(resolveGitFlowLabelTier(0.01, 2)).toBe(0);
  });
});

// ---------------------------------------------------------------------------
// P1.2 + P2.2 — priority, culling, anchor fallback, hysteresis margin.
// ---------------------------------------------------------------------------

describe("P1.2 priority collision culling + P2.2 anchor fallback", () => {
  it("higher priority wins a contested spot; fallback is bounded fork→tip→stagger", () => {
    // Three labels at (effectively) the SAME anchor and NO tip anchors: trunk
    // wins the fork slot, the newer feature takes the ONE stagger slot, the
    // older feature is CULLED — the fallback chain is bounded, no stacking.
    // (recency falls back to `x` when tipX is absent; 0.001 world ≈ 0 px.)
    const labels = [
      label({ nodeIndex: 0, name: "main", lane: 0, x: 0, y: 0 }),
      label({ nodeIndex: 1, name: "feat/old", x: 0, y: 0, tipX: undefined }),
      label({ nodeIndex: 2, name: "feat/new", x: 0.001, y: 0, tipX: undefined }),
    ];
    const p = place(labels);
    const byIndex = new Map(p.labels.map((l) => [l.nodeIndex, l]));
    expect(byIndex.get(0)?.anchor).toBe("fork");
    expect(byIndex.get(2)?.anchor).toBe("stagger"); // newer feature staggered
    expect(byIndex.has(1)).toBe(false); // older feature culled (bounded chain)
    expect(p.culled).toContain(1);
  });

  it("tip-right fallback places a label whose fork region is saturated", () => {
    const labels = [
      label({ nodeIndex: 0, name: "main", lane: 0, x: 0, y: 0, tipX: 600 }),
      // Same anchor as main, but a far tip: falls back to the tip anchor.
      label({ nodeIndex: 1, name: "feat/far-tip", x: 0, y: 0, tipX: 400 }),
    ];
    const p = place(labels);
    const far = p.labels.find((l) => l.nodeIndex === 1);
    expect(far?.anchor).toBe("tip");
    // Tip-anchored pill sits RIGHT of the tip x (clearance + half width).
    expect(far!.screenX).toBeGreaterThan((400 - CAMERA.x) * CAMERA.zoom + CAMERA.width / 2);
  });

  it("I1: accepted AABBs never intersect, even under heavy pressure", () => {
    const labels: GitFlowBranchLabel[] = [];
    for (let i = 0; i < 60; i += 1) {
      labels.push(
        label({
          nodeIndex: i,
          name: `feat/branch-${String(i).padStart(2, "0")}`,
          x: (i % 6) * 30,
          y: Math.floor(i / 6) * 6,
          tipX: (i % 6) * 30 + 120,
        }),
      );
    }
    const p = place(labels);
    expect(p.labels.length).toBeGreaterThan(0);
    expect(p.culled.length).toBeGreaterThan(0); // pressure actually culled
    for (let a = 0; a < p.labels.length; a += 1) {
      for (let b = a + 1; b < p.labels.length; b += 1) {
        const A = p.labels[a]!;
        const B = p.labels[b]!;
        const overlap =
          Math.abs(A.screenX - B.screenX) < (A.w + B.w) / 2 &&
          Math.abs(A.screenY - B.screenY) < (A.h + B.h) / 2;
        expect(overlap, `${A.text} vs ${B.text}`).toBe(false);
      }
    }
  });

  it("AC5 determinism: same scene + camera + state ⇒ identical label set", () => {
    const labels: GitFlowBranchLabel[] = [];
    for (let i = 0; i < 40; i += 1) {
      labels.push(
        label({
          nodeIndex: i,
          name: i % 3 === 0 ? `worktree-agent-${i.toString(16).padStart(7, "0")}abcdef` : `feat/b${i}`,
          x: (i % 5) * 25,
          y: Math.floor(i / 5) * 8,
          tipX: (i % 5) * 25 + 90,
        }),
      );
    }
    const run = () => place(labels, { camera: { zoom: 2 } });
    const a = run();
    const b = run();
    expect(JSON.stringify(a.labels)).toBe(JSON.stringify(b.labels));
    expect([...a.placed].sort()).toEqual([...b.placed].sort());
    expect(a.culled).toEqual(b.culled);
    expect(a.tier).toBe(2);
  });

  it("hysteresis margin: a previously-placed label keeps a knife-edge slot a new one would not get", () => {
    // Pill A at x=0; pill B's fork sits so the gap is ~4 px — inside the 8 px
    // new-label margin but NOT overlapping. Stagger for B is blocked by C.
    const mkLabels = () => [
      label({ nodeIndex: 0, name: "main", lane: 0, x: 0, y: 0 }),
      label({ nodeIndex: 1, name: "bb", x: 40, y: 0, tipX: undefined }),
    ];
    // Compute A's half width to position B at a ~4 px edge gap: instead of
    // arithmetic, probe behaviourally — B new ⇒ falls to stagger; B previously
    // placed ⇒ keeps the fork slot.
    const fresh = place(mkLabels());
    const bFresh = fresh.labels.find((l) => l.nodeIndex === 1);
    const kept = place(mkLabels(), { previous: { tier: fresh.tier, placed: new Set([0, 1]) } });
    const bKept = kept.labels.find((l) => l.nodeIndex === 1);
    expect(bFresh).toBeDefined();
    expect(bKept).toBeDefined();
    // The previously-placed run must never be MORE demoted than the fresh run.
    const order = { fork: 0, tip: 1, stagger: 2 } as const;
    expect(order[bKept!.anchor]).toBeLessThanOrEqual(order[bFresh!.anchor]);
    // And with a knife-edge gap, the anchors actually differ.
    const gapLabels = [
      label({ nodeIndex: 0, name: "wwwwwwww", lane: 0, x: 0, y: 0 }),
      label({ nodeIndex: 1, name: "wwwwwwww2", x: 0, y: 0, tipX: undefined }),
    ];
    // Same anchor: without previous, label 1 staggers; previously placed at
    // the stagger slot it keeps it — margins never let it OVERLAP label 0.
    const g1 = place(gapLabels);
    expect(g1.labels.find((l) => l.nodeIndex === 1)?.anchor).toBe("stagger");
  });
});

