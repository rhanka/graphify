/** Priority class, descending priority order (P1.2). */
export type GitFlowLabelClass =
  | "interaction" // hovered / selected / search-matched (I3, never demoted)
  | "trunk" // main/master/develop label, one per repo band
  | "release" // release/* + hotfix/*
  | "feature" // human feature branches (recency-weighted)
  | "tip-ref" // tip-only refs
  | "autogenerated"; // demoted class — present at T2 only

const CLASS_RANK: Readonly<Record<GitFlowLabelClass, number>> = {
  interaction: 0,
  trunk: 1,
  release: 2,
  feature: 3,
  "tip-ref": 4,
  autogenerated: 5,
};

/** Camera + viewport: the SAME screen transform the renderer applies —
 * `screen = (world − camera) · zoom + viewport/2` in DEVICE px. */
export interface GitFlowLabelCamera {
  x: number;
  y: number;
  zoom: number;
  /** Canvas backing-store width/height in DEVICE px. */
  width: number;
  height: number;
  /** Device pixel ratio (pill metrics scale with pixelRatio × zoom). */
  pixelRatio?: number;
}

/** Interaction state (P1.3): members render FULL-NAME at top priority. */
export interface GitFlowLabelInteraction {
  hovered?: ReadonlySet<number> | readonly number[];
  selected?: ReadonlySet<number> | readonly number[];
  searched?: ReadonlySet<number> | readonly number[];
}

/** Previous-frame state feeding both hysteresis mechanisms. */
export interface GitFlowLabelPreviousState {
  tier?: GitFlowLabelTier;
  /** nodeIndex set placed last frame (collision-margin hysteresis, P1.2). */
  placed?: ReadonlySet<number> | readonly number[];
}

export type GitFlowLabelAnchor = "fork" | "tip" | "stagger";

export interface PlacedGitFlowLabel {
  nodeIndex: number;
  /** Display text — the compacted form, or the FULL name on interaction. */
  text: string;
  /** True when `text` is the full, uncompacted branch name (I3). */
  fullName: boolean;
  cls: GitFlowLabelClass;
  /** Which anchor accepted the pill (P2.2 fallback chain). */
  anchor: GitFlowLabelAnchor;
  /** Pill CENTRE in WORLD coordinates (place the box node here). */
  x: number;
  y: number;
  /** Pill centre + extents in DEVICE px (the accepted AABB is centre ± w/2, h/2). */
  screenX: number;
  screenY: number;
  w: number;
  h: number;
}

export interface GitFlowLabelPlacement {
  tier: GitFlowLabelTier;
  /** Accepted labels, in acceptance (priority) order. */
  labels: PlacedGitFlowLabel[];
  /** Accepted nodeIndex set — feed back as `previous.placed` next frame. */
  placed: Set<number>;
  /** Candidates the tier admitted but culling rejected (diagnostics/tests). */
  culled: number[];
}

export interface GitFlowLabelPolicyInput {
  /** Anchor hints from {@link computeGitFlowPositions} (`layout.branchLabels`). */
  labels: readonly GitFlowBranchLabel[];
  /** The SAME measureText service the box draw path uses (or a deterministic
   * approximation — see {@link approximateLabelMeasure} for headless builds). */
  measure: (text: string, font: string) => number;
  camera: GitFlowLabelCamera;
  interaction?: GitFlowLabelInteraction;
  previous?: GitFlowLabelPreviousState;
}

function toSet(v: ReadonlySet<number> | readonly number[] | undefined): ReadonlySet<number> {
  if (!v) return EMPTY_SET;
  return v instanceof Set ? v : new Set(v);
}
const EMPTY_SET: ReadonlySet<number> = new Set();

/**
 * Deterministic APPROXIMATE text measurer for headless/fixture builds (no
 * canvas): a per-glyph width table that deliberately OVER-estimates average
 * sans-serif widths, so policy AABBs stay conservative vs the real
 * measureText the renderer hugs. Browser callers should pass a real
 * `ctx.measureText` instead.
 */
export function approximateLabelMeasure(text: string, font: string): number {
  const fontPx = Number.parseFloat(font) || 10;
  let units = 0;
  for (const ch of text) {
    if ("iljI.,:;!|'`·".includes(ch)) units += 0.38;
    else if ("mwMW@#%".includes(ch)) units += 0.95;
    else if (ch >= "A" && ch <= "Z") units += 0.78;
    else units += 0.62;
  }
  return units * fontPx;
}

interface Aabb {
  x0: number;
  y0: number;
  x1: number;
  y1: number;
}

/** Uniform occupancy grid over accepted AABBs (P1.2 grid occupancy). */
class OccupancyGrid {
  private readonly cell: number;
  private readonly cells = new Map<string, number[]>();
  private readonly boxes: Aabb[] = [];

  constructor(cell = 64) {
    this.cell = cell;
  }

  collides(box: Aabb): boolean {
    const cx0 = Math.floor(box.x0 / this.cell);
    const cx1 = Math.floor(box.x1 / this.cell);
    const cy0 = Math.floor(box.y0 / this.cell);
    const cy1 = Math.floor(box.y1 / this.cell);
    const seen = new Set<number>();
    for (let cy = cy0; cy <= cy1; cy += 1) {
      for (let cx = cx0; cx <= cx1; cx += 1) {
        const bucket = this.cells.get(`${cx},${cy}`);
        if (!bucket) continue;
        for (const i of bucket) {
          if (seen.has(i)) continue;
          seen.add(i);
          const b = this.boxes[i]!;
          if (box.x0 < b.x1 && box.x1 > b.x0 && box.y0 < b.y1 && box.y1 > b.y0) return true;
        }
      }
    }
    return false;
  }

  insert(box: Aabb): void {
    const index = this.boxes.length;
    this.boxes.push(box);
    const cx0 = Math.floor(box.x0 / this.cell);
    const cx1 = Math.floor(box.x1 / this.cell);
    const cy0 = Math.floor(box.y0 / this.cell);
    const cy1 = Math.floor(box.y1 / this.cell);
    for (let cy = cy0; cy <= cy1; cy += 1) {
      for (let cx = cx0; cx <= cx1; cx += 1) {
        const key = `${cx},${cy}`;
        const bucket = this.cells.get(key);
        if (bucket) bucket.push(index);
        else this.cells.set(key, [index]);
      }
    }
  }
}

/**
 * THE label policy (compaction → tier gate → priority → anchored placement).
 *
 * Placement runs in DESCENDING priority order (P1.2): interaction → trunk →
 * release/hotfix → human features (newer tip first) → tip-only refs →
 * autogenerated (T2 only). Each candidate tries the bounded anchor chain
 * (P2.2) fork-left → tip-right → ONE stagger slot; the first anchor whose
 * AABB does not overlap any accepted pill wins; otherwise the label is
 * CULLED (I1 — never drawn overlapping). New labels (absent from
 * `previous.placed`) additionally need `hysteresisMarginPx` clearance, so
 * pan/zoom does not flicker labels sitting on the knife edge.
 */
export function placeGitFlowLabels(
  input: GitFlowLabelPolicyInput,
  options: GitFlowLabelPolicyOptions = {},
): GitFlowLabelPlacement {
  const { labels, measure, camera } = input;
  const pixelRatio = camera.pixelRatio ?? 1;
  const zoom = camera.zoom;
  const margin = options.hysteresisMarginPx ?? DEFAULT_HYSTERESIS_MARGIN_PX;
  const boxHeightPx = options.boxHeightPx ?? BOX_BASE_HEIGHT_PX;
  const tipClearancePx = options.tipClearancePx ?? DEFAULT_TIP_CLEARANCE_PX;
  const staggerGapPx = options.staggerGapPx ?? DEFAULT_STAGGER_GAP_PX;

  const tier = resolveGitFlowLabelTier(zoom, input.previous?.tier, options);
  const hovered = toSet(input.interaction?.hovered);
  const selected = toSet(input.interaction?.selected);
  const searched = toSet(input.interaction?.searched);
  const previousPlaced = toSet(input.previous?.placed);

  const compacted = compactGitFlowLabels(labels, options);
  const compactedByIndex = new Map<number, CompactedGitFlowLabel>();
  for (const c of compacted) compactedByIndex.set(c.nodeIndex, c);

  interface Candidate {
    label: GitFlowBranchLabel;
    comp: CompactedGitFlowLabel;
    cls: GitFlowLabelClass;
    rank: number;
    recency: number; // tip x — newer (larger) first within `feature`
    hash: number;
  }

  const isInteraction = (i: number): boolean =>
    hovered.has(i) || selected.has(i) || searched.has(i);

  const candidates: Candidate[] = [];
  const trunkSeen = new Set<string>(); // one trunk label per repo band
  for (const label of labels) {
    const comp = compactedByIndex.get(label.nodeIndex)!;
    let cls: GitFlowLabelClass;
    if (isInteraction(label.nodeIndex)) cls = "interaction";
    else if (comp.autogenerated) cls = "autogenerated";
    else if (label.lane === 0 && !trunkSeen.has(label.repo)) {
      cls = "trunk";
      trunkSeen.add(label.repo);
    } else if (/^(release|hotfix)\//.test(comp.fullName) || /^(rel|hotfix):/.test(comp.text)) {
      cls = "release";
    } else if (label.entry === "tip-only") cls = "tip-ref";
    else cls = "feature";

    // P2.1 tier gate: T0 = trunk + interaction; T1 adds every HUMAN class;
    // T2 adds the autogenerated (demoted) class, still culling-gated.
    if (tier === 0 && cls !== "interaction" && cls !== "trunk") continue;
    if (tier <= 1 && cls === "autogenerated") continue;

    candidates.push({
      label,
      comp,
      cls,
      rank: CLASS_RANK[cls],
      recency: cls === "feature" ? (label.tipX ?? label.x) : 0,
      hash: gitFlowNameHash(comp.fullName),
    });
  }

  // P1.2 determinism: stable sort key (priority, recency, then name hash).
  candidates.sort((a, b) => {
    if (a.rank !== b.rank) return a.rank - b.rank;
    if (a.recency !== b.recency) return b.recency - a.recency;
    if (a.hash !== b.hash) return a.hash - b.hash;
    if (a.comp.fullName !== b.comp.fullName) return a.comp.fullName < b.comp.fullName ? -1 : 1;
    return a.label.nodeIndex - b.label.nodeIndex;
  });

  const sx = (wx: number): number => (wx - camera.x) * zoom + camera.width / 2;
  const sy = (wy: number): number => (wy - camera.y) * zoom + camera.height / 2;
  const wx = (px: number): number => camera.x + (px - camera.width / 2) / zoom;
  const wy = (py: number): number => camera.y + (py - camera.height / 2) / zoom;

  const grid = new OccupancyGrid();
  const placedLabels: PlacedGitFlowLabel[] = [];
  const placedSet = new Set<number>();
  const culled: number[] = [];

  const boxHeight = boxHeightPx * pixelRatio * zoom;

  for (const cand of candidates) {
    const full = cand.cls === "interaction";
    const text = full ? cand.comp.fullName : cand.comp.text;
    // Measured pill dims — the SAME boxDimensions the draw path uses, so the
    // accepted AABB is exactly the pill the renderer will draw (incl. the
    // max-width pixel-fit cap).
    const dims = boxDimensions(boxHeight, text, measure);
    const halfW = dims.w / 2;
    const halfH = dims.h / 2;
    const m = previousPlaced.has(cand.label.nodeIndex) ? 0 : margin;

    // P2.2 bounded anchor chain: fork-left → tip-right → ONE stagger slot.
    const anchors: Array<{ anchor: GitFlowLabelAnchor; cx: number; cy: number }> = [];
    const forkX = sx(cand.label.x);
    const forkY = sy(cand.label.y);
    anchors.push({ anchor: "fork", cx: forkX, cy: forkY });
    if (cand.label.tipX !== undefined && cand.label.tipX !== cand.label.x) {
      const clear = tipClearancePx * pixelRatio * zoom + 4;
      anchors.push({ anchor: "tip", cx: sx(cand.label.tipX) + clear + halfW, cy: forkY });
    }
    anchors.push({ anchor: "stagger", cx: forkX, cy: forkY - (dims.h + staggerGapPx) });

    let accepted = false;
    for (const a of anchors) {
      const test: Aabb = {
        x0: a.cx - halfW - m,
        y0: a.cy - halfH - m,
        x1: a.cx + halfW + m,
        y1: a.cy + halfH + m,
      };
      if (grid.collides(test)) continue;
      grid.insert({ x0: a.cx - halfW, y0: a.cy - halfH, x1: a.cx + halfW, y1: a.cy + halfH });
      placedLabels.push({
        nodeIndex: cand.label.nodeIndex,
        text,
        fullName: full,
        cls: cand.cls,
        anchor: a.anchor,
        x: wx(a.cx),
        y: wy(a.cy),
        screenX: a.cx,
        screenY: a.cy,
        w: dims.w,
        h: dims.h,
      });
      placedSet.add(cand.label.nodeIndex);
      accepted = true;
      break;
    }
    if (!accepted) culled.push(cand.label.nodeIndex);
  }

  return { tier, labels: placedLabels, placed: placedSet, culled };
}
