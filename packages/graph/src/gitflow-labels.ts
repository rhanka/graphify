/** An autogenerated-branch pattern: matched names are COMPACTED and DEMOTED. */
export interface GitFlowAutogeneratedRule {
  /** Applied to the prefix-stripped name. Use capture groups for `replace`. */
  pattern: RegExp;
  /** Display template; `$1`…`$9` reference capture groups. */
  replace: string;
}

/** No-information prefixes stripped before any other compaction (P1.1). */
export const DEFAULT_STRIP_PREFIXES: readonly string[] = ["refs/heads/", "origin/"];

/** Conventional prefix compaction map (P1.1), longest prefix first. */
export const DEFAULT_PREFIX_COMPACTION: Readonly<Record<string, string>> = {
  "feature/": "feat:",
  "feat/": "feat:",
  "fix/": "fix:",
  "chore/": "chore:",
  "hotfix/": "hotfix:",
  "release/": "rel:",
};

/**
 * Default autogenerated patterns (P1.1):
 *   • `worktree-agent-<hash>` → `agent:<7hex>`;
 *   • `dependabot/<eco>/<pkg>-<ver>` → `deps:<pkg>`.
 * The DEMOTION is the class, not the rewrite: matched branches get NO
 * persistent pill at T0/T1 — names are hover/selection-first (T2 shows the
 * compacted form, still culling-gated).
 */
export const DEFAULT_AUTOGENERATED_RULES: readonly GitFlowAutogeneratedRule[] = [
  { pattern: /^worktree-agent-([0-9a-f]{7})[0-9a-f]*$/i, replace: "agent:$1" },
  { pattern: /^dependabot\/[^/]+\/(.+?)[-_]v?\d[\w.-]*$/, replace: "deps:$1" },
];

/** Width budget: middle-ellipsis beyond this many characters (P1.1). */
export const DEFAULT_LABEL_MAX_CHARS = 22;
/** Collision hysteresis margin in device px (P1.2): a NEW label needs this
 * extra clearance; an ALREADY-PLACED one only needs zero overlap, so panning
 * doesn't flicker labels on the knife edge. */
export const DEFAULT_HYSTERESIS_MARGIN_PX = 8;
/** Camera zoom at which T0 (far) hands over to T1 (mid) — P2.1. */
export const DEFAULT_TIER1_ZOOM = 0.3;
/** Camera zoom at which T1 (mid) hands over to T2 (near) — P2.1. */
export const DEFAULT_TIER2_ZOOM = 1.2;
/** ±10% tier hysteresis (P2.1): no popping storms while zooming. */
export const DEFAULT_TIER_HYSTERESIS_RATIO = 0.1;
/** Horizontal clearance (CSS px, × pixelRatio × zoom) a tip-anchored pill
 * keeps off the tip commit glyph (P2.2). */
export const DEFAULT_TIP_CLEARANCE_PX = 10;
/**
 * Vertical gap (device px) of the ONE bounded stagger slot (P2.2). MUST clear
 * {@link DEFAULT_HYSTERESIS_MARGIN_PX}, otherwise a NEW label staggering away
 * from a pill at its own fork anchor could never pass the margin test and the
 * slot would be unreachable.
 */
export const DEFAULT_STAGGER_GAP_PX = 10;

export interface GitFlowLabelPolicyOptions {
  /** Prefixes stripped verbatim (default {@link DEFAULT_STRIP_PREFIXES}). */
  stripPrefixes?: readonly string[];
  /** Conventional prefix → compact form (default {@link DEFAULT_PREFIX_COMPACTION}). */
  prefixMap?: Readonly<Record<string, string>>;
  /** Autogenerated patterns (default {@link DEFAULT_AUTOGENERATED_RULES}). */
  autogenerated?: readonly GitFlowAutogeneratedRule[];
  /** Middle-ellipsis budget in characters (default 22). */
  maxChars?: number;
  /** Collision hysteresis margin in device px (default 8). */
  hysteresisMarginPx?: number;
  /** T0→T1 zoom threshold (default {@link DEFAULT_TIER1_ZOOM}). */
  tier1Zoom?: number;
  /** T1→T2 zoom threshold (default {@link DEFAULT_TIER2_ZOOM}). */
  tier2Zoom?: number;
  /** Tier hysteresis ratio (default 0.1 = ±10%). */
  tierHysteresisRatio?: number;
  /** Pill base height in CSS px (default {@link BOX_BASE_HEIGHT_PX} — the
   * SAME legacy box metric the renderer draws, so policy AABBs == drawn pills). */
  boxHeightPx?: number;
  /** Tip-anchor clearance in CSS px (default 10). */
  tipClearancePx?: number;
  /** Stagger-slot vertical gap in device px (default 2). */
  staggerGapPx?: number;
}

