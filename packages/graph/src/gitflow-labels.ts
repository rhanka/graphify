/**
 * GIT-FLOW branch-label LEGIBILITY POLICY (SPEC_GITFLOW_LABELS.md, ratified
 * (b) — Phases 1+2 together). A pure CPU-side label PRE-PASS that runs before
 * the existing `boxDimensions`/overlay draw path, per camera change:
 *
 *   branchLabels (layout anchors) ──compaction──▶ priority ──▶ placement
 *
 * and returns the PLACED label set the app/fixture builder actually emits.
 * The renderer is untouched (I4): callers simply feed fewer/renamed labels.
 *
 * Invariants implemented here:
 *   • I1 — ZERO OVERLAP: labels are placed in priority order against a
 *     screen-space occupancy grid; anything that would overlap an accepted
 *     pill AABB is dropped (MapLibre-style hard rule). Overlap is never
 *     "allowed a little".
 *   • I2 — the policy only decides NAMES; lanes/commits/connectors are the
 *     layout+renderer's business and always draw. A culled/demoted branch is
 *     reported as NOT placed — the caller renders its anchor as an EMPTY pill
 *     (or nothing), never hides its geometry.
 *   • I3 — hovered / selected / search-matched branches render their FULL,
 *     uncompacted name at TOP priority. (In the degenerate corner where two
 *     interaction labels overlap EACH OTHER, I1 wins — zero overlap is the
 *     hard invariant — deterministically by the stable sort key.)
 *   • I4 — pure functions, no renderer/shader/scene-contract coupling; every
 *     brick (compaction, tiers, culling, anchors) is independently usable.
 *   • I5 — nothing here ever touches lane geometry to "make room".
 *
 * Phase 1: P1.1 semantic compaction + autogenerated demotion (configurable
 * rules), P1.2 priority collision culling with an ~8 px hysteresis margin,
 * P1.3 interaction full-name labels. Phase 2: P2.1 zoom LOD tiers T0/T1/T2
 * with ±10% hysteresis, P2.2 bounded anchor fallback fork → tip → ONE stagger
 * slot.
 *
 * DETERMINISTIC (I4/AC5): no Date.now, no Math.random — the same scene +
 * camera (+ previous state) always yields the identical label set.
 */

import type { GitFlowBranchLabel } from "./layout-gitflow";
import { BOX_BASE_HEIGHT_PX, boxDimensions } from "./render-geometry";

