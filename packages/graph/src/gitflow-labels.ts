/** Deterministic djb2-xor name hash (stable sort key + ambiguity suffix). */
export function gitFlowNameHash(name: string): number {
  let h = 5381;
  for (let i = 0; i < name.length; i += 1) {
    h = (Math.imul(h, 33) ^ name.charCodeAt(i)) >>> 0;
  }
  return h >>> 0;
}

const ELLIPSIS = "…";

/**
 * Width-budget MIDDLE ellipsis that PRESERVES the meaningful SUFFIX
 * (`feat:…-geo-integration`, never `feat:cadrage-geo-…`). A compact prefix
 * (`feat:` etc.) survives as the head when present; the tail keeps every
 * character the budget allows.
 */
export function middleEllipsisPreservingSuffix(text: string, maxChars: number): string {
  if (maxChars <= 1 || text.length <= maxChars) return text;
  const colon = text.indexOf(":");
  const head = colon > 0 && colon < 8 ? text.slice(0, colon + 1) : "";
  const budget = Math.max(1, maxChars - head.length - 1);
  return head + ELLIPSIS + text.slice(text.length - budget);
}

export interface CompactedGitFlowName {
  /** Compacted display text (before the cross-set ambiguity guard). */
  text: string;
  /** True when an autogenerated rule matched — the DEMOTED class (P1.1). */
  autogenerated: boolean;
}

/** Compact ONE branch name (strip → autogenerated rewrite → prefix → ellipsis). */
export function compactGitFlowName(
  name: string,
  options: GitFlowLabelPolicyOptions = {},
): CompactedGitFlowName {
  const strip = options.stripPrefixes ?? DEFAULT_STRIP_PREFIXES;
  const prefixMap = options.prefixMap ?? DEFAULT_PREFIX_COMPACTION;
  const rules = options.autogenerated ?? DEFAULT_AUTOGENERATED_RULES;
  const maxChars = options.maxChars ?? DEFAULT_LABEL_MAX_CHARS;

  let text = name;
  let stripped = true;
  while (stripped) {
    stripped = false;
    for (const p of strip) {
      if (text.startsWith(p) && text.length > p.length) {
        text = text.slice(p.length);
        stripped = true;
      }
    }
  }

  for (const rule of rules) {
    const match = rule.pattern.exec(text);
    if (match) {
      const rewritten = rule.replace.replace(/\$(\d)/g, (_, d: string) => match[Number(d)] ?? "");
      return { text: middleEllipsisPreservingSuffix(rewritten, maxChars), autogenerated: true };
    }
  }

  for (const prefix of Object.keys(prefixMap)) {
    if (text.startsWith(prefix) && text.length > prefix.length) {
      text = prefixMap[prefix]! + text.slice(prefix.length);
      break;
    }
  }

  return { text: middleEllipsisPreservingSuffix(text, maxChars), autogenerated: false };
}

export interface CompactedGitFlowLabel {
  /** Index of the Branch node in the layout input order (branchLabels key). */
  nodeIndex: number;
  /** The FULL, uncompacted branch name (I3 renders this on interaction). */
  fullName: string;
  /** Compacted + ellipsized + disambiguated display text. */
  text: string;
  autogenerated: boolean;
}

/**
 * Compact a WHOLE label set with the AMBIGUITY GUARD (P1.1): when two
 * DIFFERENT full names collide on the same compacted string, each gets a
 * short deterministic hash suffix. Two branches that genuinely share a full
 * name (e.g. `main` in two repo bands) are NOT ambiguous and keep the clean
 * text. Compaction is camera-independent, so display texts are stable across
 * zoom/pan.
 */
export function compactGitFlowLabels(
  labels: readonly GitFlowBranchLabel[],
  options: GitFlowLabelPolicyOptions = {},
): CompactedGitFlowLabel[] {
  const out: CompactedGitFlowLabel[] = labels.map((label) => {
    const { text, autogenerated } = compactGitFlowName(label.name, options);
    return { nodeIndex: label.nodeIndex, fullName: label.name, text, autogenerated };
  });

  const byText = new Map<string, CompactedGitFlowLabel[]>();
  for (const c of out) {
    const list = byText.get(c.text);
    if (list) list.push(c);
    else byText.set(c.text, [c]);
  }
  for (const group of byText.values()) {
    const distinct = new Set(group.map((c) => c.fullName));
    if (distinct.size <= 1) continue; // identical full names: not ambiguous
    for (const c of group) {
      const suffix = (gitFlowNameHash(c.fullName) % 0x10000).toString(16).padStart(4, "0");
      c.text = `${c.text}·${suffix}`;
    }
  }
  return out;
}

// ---------------------------------------------------------------------------
// P2.1 — zoom LOD tiers with ±10% hysteresis.
// ---------------------------------------------------------------------------

export type GitFlowLabelTier = 0 | 1 | 2;

/**
 * Resolve the LOD tier for a camera zoom. With a `previousTier`, thresholds
 * get a ±`tierHysteresisRatio` band: moving UP requires zoom ≥ threshold ×
 * (1 + h), moving DOWN requires zoom ≤ threshold × (1 − h) — inside the band
 * the previous tier STICKS (no popping storms while zooming, P2.1).
 */
export function resolveGitFlowLabelTier(
  zoom: number,
  previousTier?: GitFlowLabelTier,
  options: GitFlowLabelPolicyOptions = {},
): GitFlowLabelTier {
  const t1 = options.tier1Zoom ?? DEFAULT_TIER1_ZOOM;
  const t2 = options.tier2Zoom ?? DEFAULT_TIER2_ZOOM;
  const h = options.tierHysteresisRatio ?? DEFAULT_TIER_HYSTERESIS_RATIO;
  if (previousTier === undefined) {
    return zoom < t1 ? 0 : zoom < t2 ? 1 : 2;
  }
  const boundary = (from: GitFlowLabelTier): number => (from === 0 ? t1 : t2);
  let tier = previousTier;
  while (tier < 2 && zoom >= boundary(tier) * (1 + h)) tier = (tier + 1) as GitFlowLabelTier;
  while (tier > 0 && zoom <= boundary((tier - 1) as GitFlowLabelTier) * (1 - h)) {
    tier = (tier - 1) as GitFlowLabelTier;
  }
  return tier;
}

